    public int findSpace()
    {
        for (int i = 0; i < 4; i++) {
            for (int j = 0; j < 4; j++) {
                if(Matriz[i][j].equals("0"))
                {
                    return j;
                }
            }
            
        }
        return 0;
    }
    
